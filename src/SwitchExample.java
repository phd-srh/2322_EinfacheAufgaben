public class SwitchExample {
    public static void main(String[] args) {

        int a = 2;

        switch (a) {
            case 1:
                System.out.println("Eins");
            case 2:
                System.out.println("Zwei");
            case 3:
                System.out.println("Drei");
            default:
                System.out.println("Geht nicht");
        }
    }
}
